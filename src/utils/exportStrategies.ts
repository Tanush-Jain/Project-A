import type { StrategyData } from '@/context/StrategyContext'
import type { StrategyOutput } from '@/data/strategy-generator'

/**
 * Generate markdown representation of strategy
 */
export function generateMarkdown(strategy: StrategyData, output: StrategyOutput): string {
  const lines: string[] = []

  lines.push(`# Strategy: ${strategy.yourTeam} vs ${strategy.opponentTeam}`)
  lines.push(`**Map:** ${strategy.map} | **Generated:** ${new Date(strategy.timestamp).toLocaleString()}`)
  lines.push('')

  // Overview
  lines.push('## 📊 Overview')
  lines.push(output.overview.matchupAnalysis)
  lines.push('')
  lines.push('### ✅ Key Advantages')
  output.overview.keyAdvantages.forEach((adv) => {
    lines.push(`- ${adv}`)
  })
  lines.push('')

  lines.push('### ⚠️ Key Threats')
  output.overview.keyThreats.forEach((threat) => {
    lines.push(`- ${threat}`)
  })
  lines.push('')

  lines.push('### 🎯 Recommended Approach')
  lines.push(`> ${output.overview.recommendedApproach}`)
  lines.push('')

  // Agent Composition
  lines.push('## 👥 Agent Composition')
  lines.push(output.agentComposition.compositionRationale)
  lines.push('')

  lines.push('| Agent | Role | Player | Reasoning |')
  lines.push('|-------|------|--------|-----------|')
  output.agentComposition.agents.forEach((agent) => {
    lines.push(`| ${agent.agent} | ${agent.role} | ${agent.player} | ${agent.reasoning} |`)
  })
  lines.push('')

  // Pistol Rounds
  lines.push('## 🔫 Pistol Rounds')

  lines.push('### 🔴 Attack')
  lines.push(output.pistolRounds.attack.strategy)
  lines.push('')
  lines.push('**Setup:**')
  output.pistolRounds.attack.setup.forEach((s) => {
    lines.push(`- ${s.player} (${s.agent}) - ${s.position}: ${s.responsibility}`)
  })
  lines.push('')
  lines.push(`**Win Condition:** ${output.pistolRounds.attack.winCondition}`)
  lines.push('')

  lines.push('### 🔵 Defense')
  lines.push(output.pistolRounds.defense.strategy)
  lines.push('')
  lines.push('**Setup:**')
  output.pistolRounds.defense.setup.forEach((s) => {
    lines.push(`- ${s.player} (${s.agent}) - ${s.position}: ${s.responsibility}`)
  })
  lines.push('')
  lines.push(`**Win Condition:** ${output.pistolRounds.defense.winCondition}`)
  lines.push('')

  // Round Timeline
  lines.push('## ⏱️ Round Timeline')

  lines.push('### 🔴 Attack')
  output.roundTimeline.gunRoundAttack.forEach((block) => {
    lines.push(`**${block.timeRange} - ${block.phase}**`)
    lines.push('Actions:')
    block.actions.forEach((a) => lines.push(`- ${a}`))
    lines.push('Key Decisions:')
    block.keyDecisions.forEach((d) => lines.push(`- ✓ ${d}`))
    lines.push('')
  })

  lines.push('### 🔵 Defense')
  output.roundTimeline.gunRoundDefense.forEach((block) => {
    lines.push(`**${block.timeRange} - ${block.phase}**`)
    lines.push('Actions:')
    block.actions.forEach((a) => lines.push(`- ${a}`))
    lines.push('Key Decisions:')
    block.keyDecisions.forEach((d) => lines.push(`- ✓ ${d}`))
    lines.push('')
  })

  // Counter Strategy
  lines.push('## 🛡️ Counter-Strategy')
  lines.push('### Opponent Weaknesses to Exploit')
  output.counterStrategy.opponentWeaknesses.forEach((w) => {
    lines.push(`- **${w.weakness}** (${w.timing}): ${w.howToExploit}`)
  })
  lines.push('')

  lines.push('### Opponent Strengths to Neutralize')
  output.counterStrategy.opponentStrengths.forEach((s) => {
    lines.push(`- **${s.strength}**: ${s.howToNeutralize} (Avoid: ${s.avoidance})`)
  })
  lines.push('')

  lines.push('### Adaptation Triggers')
  output.counterStrategy.adaptationTriggers.forEach((t) => {
    lines.push(`- **${t.situation}** → ${t.adjustment}`)
  })
  lines.push('')

  // Footer
  lines.push('---')
  lines.push('_Generated by Emergent AI Strategy Generator_')

  return lines.join('\n')
}

/**
 * Copy markdown to clipboard
 */
export async function copyToClipboard(strategy: StrategyData, output: StrategyOutput) {
  const markdown = generateMarkdown(strategy, output)
  try {
    await navigator.clipboard.writeText(markdown)
    return { success: true, message: 'Strategy copied to clipboard!' }
  } catch (error) {
    console.error('Failed to copy:', error)
    return { success: false, message: 'Failed to copy to clipboard' }
  }
}

