/**
 * Print-friendly strategy view
 */
export function openPrintView(strategy: StrategyData, output: StrategyOutput) {
  const markdown = generateMarkdown(strategy, output)

  // Convert markdown to HTML (simplified)
  let html = markdown
    .replace(/^# (.*?)$/gm, '<h1>$1</h1>')
    .replace(/^## (.*?)$/gm, '<h2>$1</h2>')
    .replace(/^### (.*?)$/gm, '<h3>$1</h3>')
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(/_(.*?)_/g, '<em>$1</em>')
    .replace(/^- (.*?)$/gm, '<li>$1</li>')
    .replace(/(<li>.*?<\/li>)/s, '<ul>$1</ul>')
    .replace(/> (.*?)$/gm, '<blockquote>$1</blockquote>')
    .replace(/\n\n/g, '</p><p>')

  html = `
    <!DOCTYPE html>
    <html>
    <head>
      <title>Strategy - ${strategy.yourTeam} vs ${strategy.opponentTeam}</title>
      <style>
        body {
          font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif;
          line-height: 1.6;
          max-width: 900px;
          margin: 0 auto;
          padding: 20px;
          color: #333;
          background: white;
        }
        h1 { font-size: 2em; margin-top: 0; color: #FF4655; }
        h2 { font-size: 1.5em; color: #0095FF; margin-top: 1.5em; }
        h3 { font-size: 1.2em; color: #0ECB81; }
        table { width: 100%; border-collapse: collapse; margin: 1em 0; }
        th, td { border: 1px solid #ddd; padding: 8px; text-align: left; }
        th { background: #f5f5f5; }
        blockquote { border-left: 4px solid #FF4655; padding-left: 1em; margin: 1em 0; }
        ul { margin: 0.5em 0; padding-left: 2em; }
        @media print {
          body { padding: 0; }
          page { break-after: always; }
        }
      </style>
    </head>
    <body>
      <p>${html}</p>
    </body>
    </html>
  `

  const printWindow = window.open('', '', 'height=600,width=800')
  printWindow?.document.write(html)
  printWindow?.document.close()
  printWindow?.print()
}
