import { useState } from 'react'
import { Link, useNavigate } from 'react-router-dom'
import { motion } from 'framer-motion'
import { Mail, Lock, Target, AlertCircle, Check } from 'lucide-react'
import { useAuth } from '@/context/AuthContext'
import { AnimatedBackground } from '@/components/AnimatedBackground'
import { GlowButton } from '@/components/GlowButton'

export default function LoginPage() {
  const [email, setEmail] = useState('')
  const [password, setPassword] = useState('')
  const [error, setError] = useState('')
  const [isLoading, setIsLoading] = useState(false)
  const { login } = useAuth()
  const navigate = useNavigate()

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    setError('')
    setIsLoading(true)

    try {
      const success = await login(email, password)
      if (success) {
        navigate('/dashboard')
      } else {
        setError('Invalid credentials. Please try again.')
      }
    } catch {
      setError('An error occurred. Please try again.')
    } finally {
      setIsLoading(false)
    }
  }

  return (
    <div className="min-h-screen bg-[#0F1923] relative flex items-center justify-center p-4">
      <AnimatedBackground />
      
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.5 }}
        className="relative z-10 w-full max-w-md"
      >
        {/* Logo */}
        <div className="text-center mb-8">
          <Link to="/" className="inline-flex items-center gap-2 group">
            <div className="relative">
              <Target className="w-10 h-10 text-[#FF4655] transition-transform group-hover:scale-110" />
              <motion.div
                className="absolute inset-0 bg-[#FF4655]/30 blur-lg rounded-full"
                animate={{ opacity: [0.3, 0.6, 0.3] }}
                transition={{ duration: 2, repeat: Infinity }}
              />
            </div>
            <span className="text-2xl font-bold text-white tracking-tight">
              VALORANT<span className="text-[#FF4655]">.ai</span>
            </span>
          </Link>
        </div>

        {/* Login Card */}
        <div className="bg-[#1C2B3A]/80 backdrop-blur-md border border-[#2B3E50] rounded-xl p-8">
          <div className="text-center mb-8">
            <h1 className="text-2xl font-bold text-white mb-2">Welcome Back</h1>
            <p className="text-[#A8B2C1] text-sm">Sign in to continue coaching</p>
          </div>

          {error && (
            <motion.div
              initial={{ opacity: 0, y: -10 }}
              animate={{ opacity: 1, y: 0 }}
              className="mb-6 p-4 bg-red-500/10 border border-red-500/30 rounded-lg flex items-center gap-3"
            >
              <AlertCircle className="w-5 h-5 text-red-400 flex-shrink-0" />
              <p className="text-red-400 text-sm">{error}</p>
            </motion.div>
          )}

          <form onSubmit={handleSubmit} className="space-y-6">
            <div className="space-y-2">
              <label className="text-sm font-medium text-[#A8B2C1]">Email</label>
              <div className="relative">
                <Mail className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-[#A8B2C1]" />
                <input
                  type="email"
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                  placeholder="coach@team.com"
                  required
                  className="w-full pl-12 pr-4 py-3 bg-[#0F1923] border border-[#2B3E50] rounded-lg text-white placeholder-[#A8B2C1]/50 focus:outline-none focus:border-[#FF4655] transition-colors"
                />
              </div>
            </div>

            <div className="space-y-2">
              <label className="text-sm font-medium text-[#A8B2C1]">Password</label>
              <div className="relative">
                <Lock className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-[#A8B2C1]" />
                <input
                  type="password"
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                  placeholder="••••••••"
                  required
                  className="w-full pl-12 pr-4 py-3 bg-[#0F1923] border border-[#2B3E50] rounded-lg text-white placeholder-[#A8B2C1]/50 focus:outline-none focus:border-[#FF4655] transition-colors"
                />
              </div>
            </div>

            <GlowButton
              type="submit"
              className="w-full"
              isLoading={isLoading}
            >
              Sign In
            </GlowButton>
          </form>

          <div className="mt-6 text-center">
            <p className="text-sm text-[#A8B2C1]">
              New coach?{' '}
              <Link to="/signup" className="text-[#FF4655] hover:text-[#ff5a68] font-medium">
                Sign up
              </Link>
            </p>
          </div>
        </div>

        {/* Back to Home */}
        <div className="mt-6 text-center">
          <Link to="/" className="text-sm text-[#A8B2C1] hover:text-white transition-colors">
            ← Back to Home
          </Link>
        </div>
      </motion.div>
    </div>
  )
}

