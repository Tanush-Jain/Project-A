export default function StrategyPage() {
  const [selectedMap, setSelectedMap] = useState('')
  const [yourTeam, setYourTeam] = useState('')
  const [opponentTeam, setOpponentTeam] = useState('')
  const [game, setGame] = useState('valorant')
  const [isGenerating, setIsGenerating] = useState(false)
  const [showResults, setShowResults] = useState(false)
  const { setCurrentStrategy, addToHistory } = useStrategy()

  const handleGenerate = async () => {
    if (!selectedMap || !yourTeam || !opponentTeam) return
    if (yourTeam === opponentTeam) return

    setIsGenerating(true)
    
    // Simulate AI generation
    await new Promise(resolve => setTimeout(resolve, 2500))

    const newStrategy = {
      id: Date.now().toString(),
      map: selectedMap,
      yourTeam,
      opponentTeam,
      timestamp: new Date(),
      winProbability: Math.floor(Math.random() * 30) + 55
    }

    setCurrentStrategy(newStrategy)
    addToHistory(newStrategy)
    setIsGenerating(false)
    setShowResults(true)
  }

  return (
    <div className="min-h-screen bg-[#0F1923] relative">
      <AnimatedBackground />
      
      <div className="relative z-10 max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 pt-24 pb-16">
        <div className="flex flex-col lg:flex-row gap-8">
          {/* Left Panel - Controls */}
          <motion.div
            initial={{ opacity: 0, x: -20 }}
            animate={{ opacity: 1, x: 0 }}
            transition={{ duration: 0.5 }}
            className="lg:w-[350px] flex-shrink-0"
          >
            <div className="bg-[#1C2B3A]/80 backdrop-blur-md border border-[#2B3E50] rounded-xl p-6 sticky top-24">
              <h2 className="text-xl font-bold text-white mb-6">Configure Strategy</h2>
              
              {/* Game Selector */}
              <div className="space-y-3 mb-6">
                <label className="text-sm font-medium text-[#A8B2C1]">Game</label>
                <div className="grid grid-cols-2 gap-2">
                  <button
                    onClick={() => setGame('valorant')}
                    className={`px-4 py-3 rounded-lg border transition-all flex items-center justify-center gap-2 ${
                      game === 'valorant'
                        ? 'bg-[#FF4655]/20 border-[#FF4655] text-white'
                        : 'bg-[#0F1923] border-[#2B3E50] text-[#A8B2C1]'
                    }`}
                  >
                    <Gamepad2 className="w-4 h-4" />
                    VALORANT
                  </button>
                  <button
                    disabled
                    className="px-4 py-3 rounded-lg border bg-[#0F1923]/50 border-[#2B3E50]/50 text-[#A8B2C1]/30 cursor-not-allowed flex items-center justify-center gap-2"
                  >
                    <Gamepad2 className="w-4 h-4" />
                    LoL
                    <span className="text-xs">(Soon)</span>
                  </button>
                </div>
              </div>

              {/* Map Selector */}
              <div className="space-y-3 mb-6">
                <label className="text-sm font-medium text-[#A8B2C1]">Map</label>
                <select
                  value={selectedMap}
                  onChange={(e) => setSelectedMap(e.target.value)}
                  className="w-full px-4 py-3 bg-[#0F1923] border border-[#2B3E50] rounded-lg text-white focus:outline-none focus:border-[#FF4655] transition-colors"
                >
                  <option value="">Select Map</option>
                  {MAPS.map(map => (
                    <option key={map} value={map}>{map}</option>
                  ))}
                </select>
              </div>

              {/* Your Team */}
              <div className="space-y-3 mb-6">
                <label className="text-sm font-medium text-[#A8B2C1]">Your Team</label>
                <select
                  value={yourTeam}
                  onChange={(e) => {
                    setYourTeam(e.target.value)
                    if (e.target.value === opponentTeam) setOpponentTeam('')
                  }}
                  className="w-full px-4 py-3 bg-[#0F1923] border border-[#2B3E50] rounded-lg text-white focus:outline-none focus:border-[#FF4655] transition-colors"
                >
                  <option value="">Select Team</option>
                  {TEAMS.filter(t => t !== opponentTeam).map(team => (
                    <option key={team} value={team}>{team}</option>
                  ))}
                </select>
              </div>

              {/* Opponent Team */}
              <div className="space-y-3 mb-8">
                <label className="text-sm font-medium text-[#A8B2C1]">Opponent Team</label>
                <select
                  value={opponentTeam}
                  onChange={(e) => {
                    setOpponentTeam(e.target.value)
                    if (e.target.value === yourTeam) setYourTeam('')
                  }}
                  className="w-full px-4 py-3 bg-[#0F1923] border border-[#2B3E50] rounded-lg text-white focus:outline-none focus:border-[#FF4655] transition-colors"
                >
                  <option value="">Select Team</option>
                  {TEAMS.filter(t => t !== yourTeam).map(team => (
                    <option key={team} value={team}>{team}</option>
                  ))}
                </select>
              </div>

              {/* Generate Button */}
              <GlowButton
                onClick={handleGenerate}
                disabled={!selectedMap || !yourTeam || !opponentTeam || yourTeam === opponentTeam}
                isLoading={isGenerating}
                className="w-full"
              >
                {isGenerating ? (
                  <span className="flex items-center justify-center gap-2">
                    <Loader2 className="w-5 h-5 animate-spin" />
                    Generating...
                  </span>
                ) : (
                  <span className="flex items-center justify-center gap-2">
                    <Sparkles className="w-5 h-5" />
                    Generate Strategy
                  </span>
                )}
              </GlowButton>

              {yourTeam === opponentTeam && yourTeam && (
                <p className="mt-3 text-sm text-red-400 text-center">
                  Please select different teams
                </p>
              )}
            </div>
          </motion.div>

          {/* Right Panel - Results */}
          <motion.div
            initial={{ opacity: 0, x: 20 }}
            animate={{ opacity: 1, x: 0 }}
            transition={{ duration: 0.5, delay: 0.2 }}
            className="flex-1"
          >
            <AnimatePresence mode="wait">
              {isGenerating ? (
                <motion.div
                  key="loading"
                  initial={{ opacity: 0 }}
                  animate={{ opacity: 1 }}
                  exit={{ opacity: 0 }}
                  className="flex flex-col items-center justify-center min-h-[500px] bg-[#1C2B3A]/30 rounded-xl border border-[#2B3E50]"
                >
                  <LoadingSpinner size="lg" />
                  <p className="mt-6 text-[#A8B2C1] animate-pulse">
                    Analyzing matchup and generating strategy...
                  </p>
                </motion.div>
              ) : showResults ? (
                <motion.div
                  key="results"
                  initial={{ opacity: 0, y: 20 }}
                  animate={{ opacity: 1, y: 0 }}
                  exit={{ opacity: 0, y: -20 }}
                >
                  {/* Strategy Header */}
                  <div className="mb-6 p-6 bg-gradient-to-r from-[#1C2B3A] to-[#1C2B3A]/50 rounded-xl border border-[#2B3E50]">
                    <div className="flex items-center gap-4 mb-4">
                      <div className="p-3 bg-[#FF4655]/20 rounded-lg">
                        <Map className="w-6 h-6 text-[#FF4655]" />
                      </div>
                      <div>
                        <h2 className="text-2xl font-bold text-white">{selectedMap}</h2>
                        <p className="text-[#A8B2C1]">
                          {yourTeam} vs {opponentTeam}
                        </p>
                      </div>
                    </div>
                    <div className="flex items-center gap-2">
                      <span className="text-sm text-[#A8B2C1]">Win Probability:</span>
                      <span className="text-2xl font-bold text-green-400">68%</span>
                    </div>
                  </div>

                  {/* Tabs */}
                  <TabSystem tabs={STRATEGY_TABS} defaultTab="overview" />
                </motion.div>
              ) : (
                <motion.div
                  key="empty"
                  initial={{ opacity: 0 }}
                  animate={{ opacity: 1 }}
                  exit={{ opacity: 0 }}
                  className="flex flex-col items-center justify-center min-h-[500px] bg-[#1C2B3A]/30 rounded-xl border border-[#2B3E50] border-dashed"
                >
                  <div className="w-20 h-20 rounded-full bg-[#1C2B3A] flex items-center justify-center mb-6">
                    <Sparkles className="w-10 h-10 text-[#FF4655]" />
                  </div>
                  <h3 className="text-xl font-bold text-white mb-2">Ready to Generate</h3>
                  <p className="text-[#A8B2C1] text-center max-w-md">
                    Select teams and click Generate to begin creating your AI-powered tactical strategy.
                  </p>
                </motion.div>
              )}
            </AnimatePresence>
          </motion.div>
        </div>
      </div>
    </div>
  )
}

